void Send_test(void)
{
    SHELL_SendBuff[0] = 'H';	//结束符
    SHELL_SendBuff[1] = 'H';	//结束符
    SHELL_SendBuff[2] = '\n';	//结束符

    UART_Send((LPC_UART_TypeDef *)LPC_UART1, SHELL_SendBuff, 3,BLOCKING);	//发送协议帧
}

/******************************************************
Function    :UartSendString
Input       :string-data
Output      :N/A
Return      :N/A
Description :串口发送字符串数据
Note        :可以直接使用"\n"这样的用法来换行。
******************************************************/
void UartSendString(LPC_UART_TypeDef* UARTx,const uint8_t *str)
{
    while(*str)
    {
        UART_SendByte(UARTx, *str++);
    }
}



